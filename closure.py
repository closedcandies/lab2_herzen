def outer_function(arg1, arg2):
  # Обычная функция, принимающая два аргумента
  def inner_function():
      # Вложенная функция (closure), использующая аргументы из внешней функции
      print(f"Переданные аргументы: arg1 = {arg1}, arg2 = {arg2}")

  # Возвращаем внутреннюю функцию
  return inner_function

# Создаем замыкание
closure = outer_function(10, 20)

# Вызываем замыкание, которое печатает аргументы внешней функции
closure()